public class App {
    public static void main(String[] args) {
        int a = 100;
        int b = 20;
        System.out.println(sum(a, b));
        System.out.println(mul(a, b));
    }

    public static int sum(int a, int b) {
        return a * b;
    }

    public static int mul(int a, int b) {
        return a * b;
    }
}
